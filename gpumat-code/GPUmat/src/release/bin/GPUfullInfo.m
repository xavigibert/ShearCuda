% GPUfullInfo - Prints information about the GPU device
% 
% SYNTAX
% 
% GPUfullInfo
% 
% 
% DESCRIPTION
% GPUfullInfo displays FULL information about each CUDA capable device
% installed on the system. Printed information includes total memory
% and number of processors. GPUfullInfo(N) displays information about
% the specific device with index= N.
% 
% EXAMPLE
% 
% GPUfullInfo(0)

